# Web Elements
###########################################################################################
# A web page is written in HTML-CSS-JS.
# An "element" refers to an HTML element on a web or elements which are present on web page.
# HTML elements are the building blocks of a web page.
# ex: Button, text box, radio buttons, dropdowns, frames, popups, autosuggestions, search bars,
# check box, scroll bars, links, etc.
# Selenium allows you to interact with these elements.

###########################################################################################

# Also webelement is an interface of selenium which has:

# Action methods:

# click(): Click on an element.
# send_keys(keys): Simulate typing into an element.
# clear(): Clear the contents of an input field.
# submit(): Submit a form. (used only for form element)

# -----------------------------------------------------------------------------------------

# Getter methods & properties:

# getText(): Gets the visible text of the element.
# getLocation(): Gets the co-ordinates of an element (x,y)
# getSize(): Gets height & width of an element.
# getAttribute(): Gets the value of the specified attribute of the element.

# these are the python properties not methods.

# title: Get the title of the current page.
# current_url: Get the URL of the current page.
# page_source: Get the source code of the current page.

# -----------------------------------------------------------------------------------------

# Verification methods:

# is_displayed(): Checks the visibility of an element
# is_enabled(): Checks enablement of an element
# is_selected(): Checks selection status of an element

